// Declare 3 variáveis para guardar suas prováveis notas e uma para sua média

public class CalculadoraMediaComVariaveis {
	
	public static void main(String[] args) {
		// Imprimindo mensagem de Boas Vindas
		System.out.println("Bem-Vindo(a) à Calculadora de Médias");
		
		//Somando as 3 notas e imprimindo a média
		double nota1 = 9.0;
		double nota2 = 7.8;
		double nota3 = 8.3;
		double media = (nota1 + nota2 + nota3) / 3;
		System.out.println(media);
	}
}
